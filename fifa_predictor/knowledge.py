def get_team_strength(team: str) -> float:
    """
    Compute a composite 0–1 strength score combining Elo, FIFA ranking, recent form
    and squad market value — all from ground-truth knowledge.
    """
    # Elo component (normalized 1300-2100 → 0-1)
    elo = ELO_RATINGS.get(team, 1500)
    elo_score = (elo - 1300) / (2100 - 1300)

    # FIFA ranking component (rank 1–210, inverted and normalized)
    rank, _ = FIFA_RANKINGS.get(team, (100, 1200))
    rank_score = max(0.0, (210 - rank) / 210)

    # Recent form component
    form = RECENT_FORM.get(team, {"w": 4, "d": 3, "l": 3, "gf": 11, "ga": 12})
    pts = form["w"] * 3 + form["d"]
    form_score = pts / 30.0  # max 30 pts in 10 games

    # Goal difference in recent form
    gd = form["gf"] - form["ga"]
    gd_score = max(0.0, min(1.0, (gd + 10) / 25))

    # Squad market value component (normalized 0-1800M)
    mv = SQUAD_VALUE_M.get(team, 100)
    mv_score = min(1.0, mv / 1800.0)

    # Weighted composite
    strength = (
        0.35 * elo_score +
        0.25 * rank_score +
        0.20 * form_score +
        0.10 * gd_score +
        0.10 * mv_score
    )
    return max(0.05, min(0.98, strength))
